/** No interaction */
export const Idle   = 'idle'

/** User hovers over the element */
export const Hover  = 'hover'

/** The element is currently active */
export const Active = 'active'

/** Default (not affected by any state) */
export const Default = 'default'

/** All available state values */
export const AvailableStates = {
    [Idle]      : Idle,
    [Hover]     : Hover,
    [Active]    : Active,
    [Default]   : Default,
}